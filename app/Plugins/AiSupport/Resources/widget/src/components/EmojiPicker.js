export class EmojiPicker {
  /**
   * @param {Object} options
   * @param {Function} options.onSelect - (emoji: string) => void
   * @param {Function} options.onClose - () => void
   */
  constructor(options) {
    this._onSelect = options.onSelect;
    this._onClose = options.onClose;
    this._el = null;
    this._gridEl = null;
    this._activeCategory = 'smileys';
    this._visible = false;
  }

  /**
   * Create and return the emoji picker DOM element.
   * @returns {HTMLElement}
   */
  render() {
    this._el = document.createElement('div');
    this._el.className = 'airpilot-emoji-picker airpilot-emoji-picker--hidden';

    // Category tabs
    const tabs = document.createElement('div');
    tabs.className = 'airpilot-emoji-tabs';

    const categoryKeys = Object.keys(EMOJI_CATEGORIES);
    for (const cat of categoryKeys) {
      const tab = document.createElement('button');
      tab.className = 'airpilot-emoji-tab' + (cat === this._activeCategory ? ' airpilot-emoji-tab--active' : '');
      tab.setAttribute('type', 'button');
      tab.textContent = t(`emoji.${cat}`);
      tab.addEventListener('click', () => this._switchCategory(cat, tabs));
      tabs.appendChild(tab);
    }
    this._el.appendChild(tabs);

    // Emoji grid
    this._gridEl = document.createElement('div');
    this._gridEl.className = 'airpilot-emoji-grid';
    this._renderCategory(this._activeCategory);
    this._el.appendChild(this._gridEl);

    return this._el;
  }

  /**
   * Show or hide the emoji picker.
   * @param {boolean} show
   */
  toggle(show) {
    this._visible = show != null ? show : !this._visible;
    if (this._el) {
      this._el.classList.toggle('airpilot-emoji-picker--hidden', !this._visible);
      if (this._visible) {
        this._el.classList.add('airpilot-emoji-picker--opening');
      }
    }
  }

  /**
   * Check if visible.
   * @returns {boolean}
   */
  isVisible() {
    return this._visible;
  }

  /**
   * Get the root element.
   * @returns {HTMLElement}
   */
  getElement() {
    return this._el;
  }

  // ─── Internal ──────────────────────────────────────────────────────────

  _switchCategory(cat, tabsContainer) {
    this._activeCategory = cat;
    this._renderCategory(cat);

    // Update tab styles
    const tabs = tabsContainer.querySelectorAll('.airpilot-emoji-tab');
    const categoryKeys = Object.keys(EMOJI_CATEGORIES);
    tabs.forEach((tab, i) => {
      tab.classList.toggle('airpilot-emoji-tab--active', categoryKeys[i] === cat);
    });
  }

  _renderCategory(cat) {
    if (!this._gridEl) return;
    this._gridEl.innerHTML = '';

    const emojis = EMOJI_CATEGORIES[cat] || [];
    for (const emoji of emojis) {
      const btn = document.createElement('button');
      btn.className = 'airpilot-emoji-btn';
      btn.setAttribute('type', 'button');
      btn.textContent = emoji;
      btn.addEventListener('click', () => {
        if (this._onSelect) this._onSelect(emoji);
      });
      this._gridEl.appendChild(btn);
    }
  }
}
